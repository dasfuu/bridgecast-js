function sendSysEx(bytes) {
    if (output){
        // console.log("SEND SYSEX: "+bytes)
        output.send(bytes);
    }
}

function sendSysExBatch(messages, delay = 0) {
    if (!output) return;

    messages.forEach((msg, index) => {
        if (delay > 0) {
            setTimeout(() => sendSysEx(msg), index * delay);
        } else {
            sendSysEx(msg);
        }
    });
}

function sendAllNotesOffAndReset() {
    if (!output2) return;

    for (let ch = 1; ch <= 16; ch++) {
        const status = 0xB0 | ((ch - 1) & 0x0F);

        // CC 123 = All Notes Off
        output2.send([status, 123, 0]);

        // CC 121 = Reset All Controllers
        output2.send([status, 121, 0]);
    }
}

