
let isUpdatingFromMIDI = false;

let input = null;
let output2 = null;
let output = null;

async function startMIDI() {



    const midi = await navigator.requestMIDIAccess({ sysex: true });
    for (let entry of midi.inputs.values()) {
        console.log("In: " + entry.name);
        if (entry.name === "BRIDGE CAST X-I V2 MIDI 1") {
            input = entry;
            break;
        }
    }
    for (let entry of midi.outputs.values()) {
        console.log("Out: "+entry.name)
        if (entry.name === "BRIDGE CAST X-I V2 MIDI 1") {
            output = entry;
        }
        if (entry.name === "BRIDGE CAST X-I V2 MIDI 2") {
            output2 = entry;
        }
    }
    if (!output) {
        alert("No MIDI device found");
        return;
    }
    if (!input) {
        alert("No MIDI device found");
        return;

    }
    console.log("Using:", input.name);
    console.log("Using Out1:", output.name);
    console.log("Using Out2:", output2.name);
    input.onmidimessage = (event) => {

        const data = Array.from(event.data);
        // filter typical SysEx size
        if (data[0] === 0xF0) {
            parseSysEx(data);
        }

    };

    console.log("Listening...");
    sendAllNotesOffAndReset();
    sendBridgeFullInit();
    // sendBridgeFullInit2();
    // sendBridgeInit();
    // window.setInterval(sendBridgeInit, 100);

}

function sendCC(channel, cc, value) {
    if (!output2) return;

    const status = 0xB0 | ((channel - 1) & 0x0F); // CC message
    output2.send([status, cc, value]);

}
function setEnabled(input, type, enabled) {
    if (!isUpdatingFromMIDI) {
        if(type === 'output') {
            sendCC(1, input.control_channel, enabled ? 127 : 0);
            return;
        }
        let cc = 19;
        if(type === 'stream') {
            cc = 18;
        } else if(type === 'personal') {
            cc = 19;
        } else if(type === 'mic') {
            cc = 20;
        }
        sendCC(input.control_channel, cc, enabled ? 127 : 0);
    }

}

function parseSysEx(data) {
    let known = false;
    if (data.length == 17) {
        const category = data[11];
        const address = data[12];
        const value = data[14];


        isUpdatingFromMIDI = false;

        const fader = sysExFader?.[category]?.[address];
        if (fader) {
            isUpdatingFromMIDI = true;
            fader.value = value;
            known = true;
        }
        const label = sysExLabel?.[category]?.[address];
        if (label) {
            isUpdatingFromMIDI = true;
            label.innerText = value;
            known = true;
        }
        const toggle = sysExToggle?.[category]?.[address];
        if (toggle) {
            isUpdatingFromMIDI = true;
            toggle.checked = value > 0;
            known = true;
        }

        isUpdatingFromMIDI = false;

        if(!known) {
            console.log("Input SysEx: "+data)
            known = true;
        }
    }
    if (data.length === 121) {
        if(data[10] === 3) {
            known = true;
            parseChannelLevel(data);
        }
        if(!known) {
            console.log("Input 121: "+data)
            known = true;
        }
    }
    if(!known) {
        //console.log("Unknown SysEx: "+data)
    }
}

